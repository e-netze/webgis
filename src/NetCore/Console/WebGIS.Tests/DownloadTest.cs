    async public Task Run()
    {
        string[] urls = Urls1;

        var httpService = new HttpService(null, null, null);

        // Sync
        Console.WriteLine("Downloading synchronously:");
        var sw = new System.Diagnostics.Stopwatch();
        sw.Start();
        foreach (var url in urls)
        {
            var d = await httpService.GetDataAsync(url);
            Console.WriteLine(d.Length);
        }
        sw.Stop();
        Console.WriteLine($"Done in {sw.ElapsedMilliseconds} ms");

        // Async
        Console.WriteLine("Downloading Async:");
        sw = new System.Diagnostics.Stopwatch();
        sw.Start();
        var data = urls.Select(u => new UrlData()
        {
            Url = u
        });

        data = await httpService.GetDataAsync(data);
        foreach (var d in data)
        {
            Console.WriteLine(d.Data.Length);
        }
        sw.Stop();
        Console.WriteLine($"Done in {sw.ElapsedMilliseconds} ms");
    }
